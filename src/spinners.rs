impl Spinners {
    pub fn get_frames(&self) -> Result<Vec<&'static str>, Error> {
        if let Some(frames) = SPINNER_MAP.get(&self.to_string()) {
            Ok(frames.to_vec())
        } else {
            Err(Error::UnknownSpinner(self.to_string()))
        }
    }
}

